package org.perscholas.objectpractice;

public class Cat {
    // name
    public String name;
    // color
    public String color;
    // weight
    public int weight;
    // age
    public int age;

    public Cat(String name, String color, int weight, int age){
        this.name = name;
        this.color = color;
        this.weight = weight;
        this.age = age;
    }
}
